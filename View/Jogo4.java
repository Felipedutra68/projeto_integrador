    public void tocarSomEmLoop(String caminhoDoSom) {
        try {
            // Carrega o arquivo de som
            File arquivoSom = new File(caminhoDoSom);
            AudioInputStream audioInputStream = AudioSystem.getAudioInputStream(arquivoSom);
            AudioFormat formato = audioInputStream.getFormat();
            DataLine.Info info = new DataLine.Info(Clip.class, formato);
            clip = (Clip) AudioSystem.getLine(info);
            clip.open(audioInputStream);

            // Toca o som em loop
            clip.loop(Clip.LOOP_CONTINUOUSLY);
            clip.start();

        } catch (UnsupportedAudioFileException | IOException | LineUnavailableException e) {
            e.printStackTrace();
        }
    }

    public void pararLoop() {
        if (clip != null && clip.isRunning()) {
            clip.stop();
            clip.close();
        }
    }

